class VäikeRuut {
    int rida, veerg;

    public VäikeRuut(int rida, int veerg) {
        this.rida = rida;
        this.veerg = veerg;
    }

    public int getRida() {
        return rida;
    }

    public int getVeerg() {
        return veerg;
    }
}
